#ifndef GAME_H
#define GAME_H

class Player;
class Dice;

#include <iostream>
#include <vector>
#include "Board.h"
#include <stdlib.h>

class Game
{
public:
    //-------------------------------------------------------------------------
    /// @brief default constructor
    //-------------------------------------------------------------------------
    Game();



    //-------------------------------------------------------------------------
    /// @brief method that starts a game
    //-------------------------------------------------------------------------
    void StartGame();

    //-------------------------------------------------------------------------
    /// @brief method that plays the game
    //-------------------------------------------------------------------------
    void PlayGame();

    //-------------------------------------------------------------------------
    /// @brief method that takes a turn
    //-------------------------------------------------------------------------
    void TakeTurn(Player *player);

    //-------------------------------------------------------------------------
    /// @brief method that prints to the screen with ncurses
    //-------------------------------------------------------------------------
    void PrintScreen(std::string message);

    //-------------------------------------------------------------------------
    /// @brief method that clears the screen with ncurses
    //-------------------------------------------------------------------------
    void ClearScreen();
    
    //-------------------------------------------------------------------------
    /// @brief method that gets input with ncurses
    //-------------------------------------------------------------------------
    std::string ReadInput();


    //-------------------------------------------------------------------------
    /// @brief method that resets a game
    //-------------------------------------------------------------------------
    void reset();

    //-------------------------------------------------------------------------
    /// @brief default destructor
    //-------------------------------------------------------------------------
    ~Game();

private:
    //-------------------------------------------------------------------------
    /// @brief the board of the game
    //-------------------------------------------------------------------------
    Board m_board;

    //-------------------------------------------------------------------------
    /// @brief method that read the name of the next tile
    //-------------------------------------------------------------------------
    std::string readTilesName(
            const std::vector<std::string> &i_words,
            unsigned int *i_p
            );
    //-------------------------------------------------------------------------
    /// @brief method that sets up the Game
    //-------------------------------------------------------------------------
    void SetupGame();


    //-------------------------------------------------------------------------
    /// @brief the number of the current Player
    //-------------------------------------------------------------------------
    int m_currentPlayer;
    //-------------------------------------------------------------------------
    /// @brief all the players for the game
    //-------------------------------------------------------------------------
    std::vector<Player *> m_players;

    //-------------------------------------------------------------------------
    /// @brief the game dice
    //-------------------------------------------------------------------------
    Dice* m_dice;
};

#endif // GAME
