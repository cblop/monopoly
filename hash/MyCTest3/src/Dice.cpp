#include <stdlib.h>
#include "Dice.h"


//-----------------------------------------------------------------------------
Dice::Dice()
{
    roll();
}

//-----------------------------------------------------------------------------
void Dice::roll()
{
    m_values[0] = (rand() % 6) + 1;
    m_values[1] = (rand() % 6) + 1;
}

//-----------------------------------------------------------------------------
int *Dice::getValues()
{
    return m_values;
}

int Dice::getTotal()
{
    return (m_values[0] + m_values[1]);
}

bool Dice::isDouble()
{
    return (m_values[0] == m_values[1]);
}


//-----------------------------------------------------------------------------
Dice::~Dice()
{}
