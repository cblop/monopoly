#include <stdlib.h>
#include "Dice.h"


//-----------------------------------------------------------------------------
Dice::Dice()
{
    roll();
}

//-----------------------------------------------------------------------------
void Dice::roll()
{
    m_value = (rand() % 12) + 1;
}

//-----------------------------------------------------------------------------
int Dice::getValue()
{
    return m_value;
}


//-----------------------------------------------------------------------------
Dice::~Dice()
{}
