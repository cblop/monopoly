#include <ctime>
#include <stdlib.h>
#include "Dice.h"


//-----------------------------------------------------------------------------
Dice::Dice()
{
    roll();
}

//-----------------------------------------------------------------------------
void Dice::roll()
{
    srand((unsigned)time(0));
    m_value = (rand() % 12) + 1;
}

//-----------------------------------------------------------------------------
int Dice::getValue()
{
    return m_value;
}
